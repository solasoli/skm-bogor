              <h4>Pemerintah Daerah sebagai penyedia layanan dituntut
                  untuk senantiasa memberikan pelayanan prima
                  kepada masyarakat. Keberhasilan Pemerintah Daerah
                  dalam menjalankan peranannya tersebut sangat ditentukan
                  pada tingkat kualitas pelayanan publik yang disediakan.
                  <br>Untuk mengetahui sejauh mana pelayanan publik
                  mampu memenuhi harapan masyarakat di suatu daerah
                  maka diperlukan upaya-upaya untuk selalu memperbaiki
                  pelayanan sehingga sesuai dengan perkembangan zaman
                  dan harapan masyarakat pada saat ini.
                  <br>Salah satu upaya
                  untuk memperbaiki dan meningkatkan kualitas pelayanan
                  publik tersebut adalah dengan melakukan evaluasi
                  untuk menilai kinerja pelayanan yang disediakan oleh berbagai
                  Perangkat Daerah melalui pelaksanaan Survei Kepuasan
                  Masyarakat.
                  <br>Kualitas pelayanan publik merupakan sebuah indikator utama
                  dalam penyelenggaraan pemerintahan yang baik. Berhasil
                  atau tidaknya pelayanan kepada masyarakat tidak akan
                  pernah dapat diketahui tanpa pernah dilakukan suatu
                  pengukuran. Pengukuran kepuasan masyarakat merupakan
                  elemen penting dalam proses evaluasi kinerja dimana tujuan
                  akhir yang hendak dicapai adalah menyediakan pelayanan
                  yang lebih baik, lebih efisien, dan lebih efektif berbasis
                  dari kebutuhan masyarakat.
                  <br>Suatu pelayanan dinilai
                  memuaskan apabila pelayanan tersebut dapat memenuhi
                  kebutuhan dan harapan pengguna layanan.
                  Kepuasan masyarakat dapat juga dijadikan acuan bagi berhasil
                  atau tidaknya pelaksanaan program yang dilaksanakan
                  pada suatu penyelenggara pelayanan publik.
                  <br>
                  <br><b>Survei Kepuasan Masyarakat (SKM)</b> adalah pengukuran
                  secara komprehensif kegiatan tentang tingkat kepuasan
                  masyarakat yang diperoleh dari hasil pengukuran
                  atas pendapat masyarakat. <br>Melalui survei ini diharapkan
                  mendorong partisipasi masyarakat sebagai pengguna layanan
                  dalam menilai kinerja penyelenggara pelayanan publik
                  serta mendorong penyelenggara pelayanan publik
                  untuk meningkatkan kualitas pelayanan dan melakukan
                  pengembangan melalui inovasi-inovasi pelayanan publik.
